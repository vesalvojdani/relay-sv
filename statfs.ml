(** Interface to the statfs syscall. Find out how many blocks are free, etc. *)

type statfs = {
  f_type : int64;
  f_bsize : int64;
  f_blocks : int64;
  f_bfree : int64;
  f_bavail : int64;
  f_files : int64;
  f_ffree : int64;
  f_fsid : unit;
  f_namelen : int64;
}

external statfs : string -> statfs = "statfs_statfs"

external fstatfs : Unix.file_descr -> statfs = "statfs_fstatfs"
